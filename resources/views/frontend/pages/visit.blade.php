        <script>
            // Get all the tabs in the form
            const tabs = document.querySelectorAll(".tab");

            // Function to show the current tab and hide other tabs
            function showTab(currentTab) {
                tabs.forEach((tab) => {
                    tab.style.display = "none";
                });
                currentTab.style.display = "block";
            }

            // Function to handle checkbox click and navigate to the next tab with a delay
            function handleCheckboxClick(event) {
                // Get the closest parent tab of the clicked checkbox
                const currentTab = event.target.closest(".tab");

                // Check if the currentTab is the last tab, if so, do not navigate further
                if (currentTab.nextElementSibling !== null) {
                    // Show the next tab after a 2-second delay
                    setTimeout(() => {
                        showTab(currentTab.nextElementSibling);
                    }, 1000); // 1 seconds delay
                }
            }

            // Get all the checkboxes in the form
            const checkboxes = document.querySelectorAll('input[type="checkbox"]');

            // Add event listener to each checkbox
            checkboxes.forEach((checkbox) => {
                checkbox.addEventListener("click", handleCheckboxClick);
            });

            // By default, show the first tab when the page loads
            showTab(tabs[0]);
        </script>
